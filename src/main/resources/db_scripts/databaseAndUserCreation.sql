CREATE ROLE admin WITH
	LOGIN
	NOSUPERUSER
	CREATEDB
	CREATEROLE
	INHERIT
	REPLICATION
	CONNECTION LIMIT -1
	PASSWORD 'password';

CREATE DATABASE university_database
    WITH 
    OWNER = postgres
    ENCODING = 'UTF8'
    CONNECTION LIMIT = -1;
   
GRANT ALL PRIVILEGES ON DATABASE university_database TO admin;
